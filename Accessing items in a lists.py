list3 = [num for num in range(100)]
evennumbers = [num*2 for num in range(100)]

print("Range in this list : ",list3)
print("All the list : ",evennumbers)
print("What is the 7th number in this list : ",evennumbers[7])
print("What is the last number in this list : ",evennumbers[-1])
print("What is the 3rd very last number in this list : ",evennumbers[-3])
# Slicing (selecting) a list - Slicing opperation
print("What is the 1st and 3rd numbers in this list : ",evennumbers[1:3])
print("What are the numbers 3rd index onward in this list : ",evennumbers[3:])





